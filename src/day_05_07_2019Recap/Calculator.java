package day_05_07_2019Recap;

public class Calculator {
	
	public int add(int a, int b) {
		int sum=a+b;
		return sum;
	}
public static int substract(int a, int b) {
	int result=a-b;
	return result;
}
public static int multiply(int a, int b) {
	return a*b;
}
public double division(int a, int b) {
    return a/b;
}
}